package dashboard.backend.controller;


import dashboard.backend.model.user.User;
import dashboard.backend.repository.UserRepository;
import dashboard.backend.service.UserService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/user")
public class UserController {

    private final UserRepository userRepository;

    private final UserService userService;

    public UserController(UserRepository userRepository, UserService userService) {
        this.userRepository = userRepository;
        this.userService = userService;
    }

    @PostMapping("/sign")
    public ResponseEntity<User> cadastrarUser(@RequestBody User user) {
        User userSalvo = userService.sign(user);
        return ResponseEntity.status(HttpStatus.CREATED).body(userSalvo);
    }
}
